	/// <summary>
	/// Represents a Unicode category
	/// </summary>
    public class UnicodeCategory
    {
		/// <summary>
		/// The category's name
		/// </summary>
        private string name;

		/// <summary>
		/// The list of character spans contained in this category
		/// </summary>
        private List<CharSpan> spans;

		/// <summary>
		/// Gets this unicode category's name
		/// </summary>
        public string Name { get { return name; } }

		/// <summary>
		/// Gets the character spans contained by this category
		/// </summary>
        public List<CharSpan> Spans { get { return spans; } }

		/// <summary>
		/// Adds a span to this category
		/// </summary>
		/// <param name="begin">The span's beginning character</param>
		/// <param name="end">The span's ending character</param>
		private void AddSpan(ushort begin, ushort end)
		{
			spans.Add(new CharSpan(System.Convert.ToChar(begin), System.Convert.ToChar(end)));
		}

		/// <summary>
		/// Initializes a new (empty) category
		/// </summary>
		/// <param name="name">The category's name</param>
        private UnicodeCategory(string name)
        {
            this.name = name;
            this.spans = new List<CharSpan>();
        }

		/// <summary>
        /// Gets the string representation of this category
        /// </summary>
        /// <returns>The string representation</returns>
        public override string ToString()
        {
			return name;
        }

		/// <summary>
		/// The cache of the standard Unicode categories
		/// </summary>
        private static Dictionary<string, UnicodeCategory> categories = null;

		/// <summary>
		/// Gets the supported Unicode categories
		/// </summary>
		/// <returns>The supported Unicode categories</returns>
		public static ICollection<UnicodeCategory> GetBlocks()
		{
			if (categories == null)
				BuildCategories();
			return categories.Values;
		}

		/// <summary>
		/// Gets the Unicode category with the given name
		/// </summary>
		/// <param name="name">A category's name</param>
		/// <returns>The corresponding cateogry, or null if none exists</returns>
		public static UnicodeCategory GetCateogry(string name)
		{
			if (categories == null)
				BuildCategories();
			return categories[name];
		}

